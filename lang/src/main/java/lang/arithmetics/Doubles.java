package lang.arithmetics;

public class Doubles
{
    public static void main(String[] args)
    {
        System.out.println((60.0 / 9.81) * 3.711 == 22.69724770642202);
        System.out.println((60.0 / 9.81) * 24.79 == 151.62079510703364);

        System.out.println(20 * 365 / 88);
        System.out.println(Math.round(60 / 29.5));
    }
}
